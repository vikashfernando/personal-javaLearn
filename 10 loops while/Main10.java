public class Main10{
    public static void main(String[] args){

        //print numbers 1-9(do somthing for ptimes)
        int i=1;
        while(i<10){
            System.out.println(i);
            i++;
        }

        //print numbers 0-12(do somthing for 13times)
        i=0;
        while(i<=12){
            System.out.println(i);
            i++;
        }


        //print numbers 20-5
        i=20;
        while(i>=5){
            System.out.println(i);
            i--;
        }
    }
}